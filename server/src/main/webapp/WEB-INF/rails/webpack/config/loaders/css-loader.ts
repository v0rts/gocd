import {ConfigOptions} from "config/variables";
import MiniCssExtractPlugin from "mini-css-extract-plugin";
import path from "path";
import webpack from "webpack";

export function getMiniCssExtractLoader(configOptions: ConfigOptions): webpack.RuleSetRule {
  return {
    test: /\.(s)?css$/,
    exclude: /node_modules/,
    use: [
      {
        loader: configOptions.production ? MiniCssExtractPlugin.loader : "style-loader",
        options: {
          esModule: false // we need CommonJS modules
        }
      },
      {
        loader: path.resolve(__dirname, "type-declarations-for-css-modules.js"),
        options: {
          banner: "// This file is automatically generated. Please do not change this file!"
        }
      },
      {
        loader: "css-loader",
        options: {
          modules: {
            mode: "local",
            localIdentName: "[name]__[local]___[hash:base64:5]",
            exportLocalsConvention: "camelCase",
            exportOnlyLocals: false
          },
          sourceMap: true,
          importLoaders: 1
        }
      },
      {
        loader: "sass-loader", // compiles Sass to CSS, defaults to Dart Sass
        options: {
          sourceMap: true
        }
      }
    ]
  };
}
